export const useI18n = () => {
  const t = (key: string): TranslationValue => messages[localeState.current][key] ?? key;

  const setLocale = (nextLocale: AppLocale): void => {
    localeState.current = nextLocale;
  };

  return {
    localeState,
    t,
    setLocale
  };
};